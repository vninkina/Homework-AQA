public class Employee {
    String lastName;
    String firstName;
    String middleName;
    String email;
    String phone;
    int salary;
    int age;

    public Employee(String lastName, String firstName, String middleName, String email,
                    String phone, int salary, int age) {
        this.lastName = lastName;
        this.firstName = firstName;
        this.middleName = middleName;
        this.email = email;
        this.phone = phone;
        this.salary = salary;
        this.age = age;
    }
}

